/*

* Each method accepts a function as an argument

---forEach:
 applies the function to each element and returns nothing

---map:
 returns an array with the function applied to each element

---filter:
 applies the function to each element, and returns an array with only those elements for which it returns true

---some/every:
 returns true if the function returns true on any/all of the elements and false otherwise. It may not run on the entire array

---reduce:
 applies the function to each element, and allows for an “accumulator” to be passed into the next iteration

*/